    package com.Ananda.Modul3.Agregasi;

    public class Mahasiswa {
        private String nama;
        private int nim;

        //constructor
        public Mahasiswa(String nama, int nim) {
            this.nama = nama;
            this.nim = nim;
        }

        //getter
        public String getNama() {
            return nama;
        }

        //getter
        public int getNim() {
            return nim;
        }

    }
